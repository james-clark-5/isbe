        set(ui.axes_l,...
            'Position', axes_pos_l,...
            'Xlim', [0.5 axes_pos_l(3)+0.5],...
            'Ylim', [0.5 axes_pos_l(4)+0.5]);
       
        %Compute mammo limits for color scaling
        max_mammo = double(max([max(mam_r(:)) max(mam_l(:))]));
        min_mammo = double(min([min(mam_r(:)) min(mam_l(:))]));
        
        set(ui.axes_r, 'Clim', [min_mammo max_mammo]);
        set(ui.axes_l, 'Clim', [min_mammo max_mammo]);
        
        %Make the mammograms visible
        set(ui.region_r,...
            'Visible', 'on',...
            'CData', mam_r,...
            'XData', [1 axes_pos_r(3)],...
            'YData', [1 axes_pos_r(4)]);
        set(ui.region_l,...
            'Visible', 'on',...
            'CData', mam_l,...
            'XData', [1 axes_pos_l(3)],...
            'YData', [1 axes_pos_l(4)]);
        
        set(ui.meta_r,...
            'XData', axes_pos_r(3)*meta_rx,...
            'YData', axes_pos_r(4)*meta_ry);
        set(ui.meta_l,...
            'XData', axes_pos_l(3)*meta_lx,...
            'YData', axes_pos_l(4)*meta_ly);
        
        set(ui.min_m_slider,...
            'Min', min_mammo,...
            'Max', 0.99*max_mammo,...
            'Value', min_mammo,...
            'Enable', 'on')
        set(ui.max_m_slider,...
            'Min', 1.01*min_mammo,...
            'Max', max_mammo,...
            'Value', max_mammo,...
            'Enable', 'on')
        
        mam_r = [];
        mam_l = [];
        set(ui.min_m_slider_text,...
            'String', ['Select min/max values of mammo contrast range: [' num2str(min_mammo,3) ', ' num2str(max_mammo,3) ']']);
        
    end
%----------------------------------------------------------------------
    function update_curr_pair
        
        set(ui.pair_number_text, 'String', ['Select mammogram pair:' num2str(curr_pair) ' of ' num2str(num_pairs)]);
        set(ui.pair_number_selecter, 'Value', curr_pair);
        
        if curr_pair == num_pairs
            set(ui.next_pair, 'Enable', 'off');
        else
            set(ui.next_pair, 'Enable', 'on');
        end
        if curr_pair == 1
            set(ui.previous_pair, 'Enable', 'off');
        else
            set(ui.previous_pair, 'Enable', 'on');
        end

        %Work out the case number and view type for this pair of maps
        pair_num = pair_strings{curr_pair}(1:3);
        pair_view = pair_strings{curr_pair}(5:6);
        
        %Get names of orientation maps for this pair
        ori_names_r = dir([ori_dir '\*' pair_num 'R' pair_view '*.mat']);
        ori_names_l = dir([ori_dir '\*' pair_num 'L' pair_view '*.mat']);

        %Load in the maps and mammograms and update the display
        load_mammograms;
        load_maps;
    end
