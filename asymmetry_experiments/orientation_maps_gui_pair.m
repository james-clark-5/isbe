%--------------------------------------------------------------------------
    function update_quivers
        
        if curr_smoothing
            %Smooth the maps by sigma
            smooth_map_r = imfilter(ori_map_r, fspecial('gaussian', round(6*curr_smoothing+1), curr_smoothing));
            smooth_map_l = imfilter(ori_map_l, fspecial('gaussian', round(6*curr_smoothing+1), curr_smoothing));
        else
            smooth_map_r = ori_map_r;
            smooth_map_l = ori_map_l;
        end
        smooth_ori_r = angle(smooth_map_r.^2);
        smooth_ori_l = angle(smooth_map_l.^2);  
        
        for ii = 1:num_angles
            theta_min = (ii - 1)*ang_res - pi;
            theta_max = ii*ang_res - pi;
            
            %Get mask of pixels that have orientation within theta range
            
            %Right
            angle_mask = mask_r & ...
                         (smooth_ori_r > theta_min) &...
                         (smooth_ori_r <= theta_max);
            [yy xx] = find(angle_mask);

            set(ui.arrows_r(ii),...
                'Xdata', axes_pos_r(3)*xx/cols_r - q_length*real(smooth_map_r(angle_mask)),...
                'Ydata', axes_pos_r(4)*yy/rows_r + q_length*imag(smooth_map_r(angle_mask)), ...
                'Udata', 2*q_length*real(smooth_map_r(angle_mask)),...
                'Vdata',-2*q_length*imag(smooth_map_r(angle_mask)),...
                'LineWidth', 1,...
                'Visible', 'on');
           
            %Left
            angle_mask = mask_l & ...
                         (smooth_ori_l > theta_min) &...
                         (smooth_ori_l <= theta_max);
            [yy xx] = find(angle_mask);

            set(ui.arrows_l(ii),...
                'Xdata', axes_pos_l(3)*xx/cols_l - q_length*real(smooth_map_l(angle_mask)),...
                'Ydata', axes_pos_l(4)*yy/rows_l + q_length*imag(smooth_map_l(angle_mask)), ...
                'Udata', 2*q_length*real(smooth_map_l(angle_mask)),...
                'Vdata',-2*q_length*imag(smooth_map_l(angle_mask)),...
                'LineWidth', 1,...
                'Visible', 'on');
        end
        clear smooth*      
        
    end

